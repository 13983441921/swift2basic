//: 字符串和字符
//:
//: ----
//:
//:  - 一个字符串是一个有序的字符集合.如"Hello树先生"和"侏罗纪世界".
//:  - Swift用String来表示字符串类型.它是Character类型值的集合.
//:  - String和Character快速,且与Unicode(万国码)兼容.
//:  - 2个字符串可以通过 + 来连接.
//:  - 可以通过字符串插值来组合常量/变量/字面量/表达式成一个长字符串.

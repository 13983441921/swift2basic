//: 闭包 Closure
//:
//: ----
//:  - 闭包是函数的一种原始状态，也是一段代码块，执行一个特定任务。但闭包是无名称的。
//:  - 之所以得名，源于它看似自身范围有限，但却可以“捕获”外部变量，相对于把他们无形地“包围”。
//:  - 全局函数是有名有姓的闭包，无需“捕获”。
//:  - 嵌套函数是寄人篱下的闭包，靠“捕获”父函数的变量为生。
//:  - 闭包是无名的、有着精简语法，可以“捕获”周围的值。
//: ----
//: ----
//:  - Swift中的闭包有着：
//:  - 1.参数和返回值类型推断
//:  - 2.可隐式返回
//:  - 3.快捷参数名
//:  - 4.可尾随的语法
